no_tags_training = []
everything_else_training = []

def preprocess_function(example):
  if "No XBRL tags found" in example["output"]:
    no_tags_training.append(example)
  else:
    everything_else_training.append(example)


training_data = None
with open("train.json", "r") as f:
  training_data = json.load(f)

for example in training_data:
    preprocess_function(example)


